package C3;

public class AssignmentOperator {
    public static void main(String[] args) {

        int number1= 88;
        number1 += 50;
        System.out.println(number1);

        number1 -= 60;
        System.out.println(number1);

        number1 *= 60;
        System.out.println(number1);

        number1 /= 60;
        System.out.println(number1);

        number1 %= 60;
        System.out.println(number1);
    }
}

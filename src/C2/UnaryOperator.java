package C2;

public class UnaryOperator {
    public static void main(String[] args) {
        int number = 85;
        System.out.println(number++);
        System.out.println(number);
        System.out.println(--number);
        System.out.println(number);
        System.out.println(number--);
        System.out.println(++number);
        System.out.println(number);
    }
}
